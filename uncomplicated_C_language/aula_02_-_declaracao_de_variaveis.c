#include <stdio.h>
#include <stdlib.h>

int main(){

  char letra = 'a';
  char nro = 10;
  int _nro = 10;
  float n = 5.25;
  double d = 5.0e3;

  int nro1 = 10;
  int nro2;
  int nro3;
  int nro1 = 10, nro2, nro3;

  return 0;
}

/*
tipo nome;

char - caracteres
int - números inteiros
float - números reais
double - números reais com maior precisão

Para nomear uma variável posso usar:
- letras maiúsculas/minúsculas;
- números; (não pode começar)
-  _ .

Não pode ser usado: 
- letras com acentuação;
- símbolos, exceto o _;
- palavras reservadas.

Case sensitive
*/